package com.lrn.jav.chair;

public abstract class AChair {

    public final void seatingFunction() {
        System.out.println("AChair : I can sit");
    }

    public final void armSupportFunction() {
        System.out.println("AChair : I have arm support");
    }

    public final void leanBackSupportFunction() {
        System.out.println("AChair : I can lean back");
    }

}
